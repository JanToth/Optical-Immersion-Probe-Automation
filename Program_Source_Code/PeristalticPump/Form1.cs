        private void backgroundWorker1_DoWork(object sender, DoWorkEventArgs e)
        {             
            try
            {                
                for (int i = 0; i < Convert.ToUInt16(textBox5.Text); i++)
                {

                    if (backgroundWorker1.CancellationPending)
                    {
                        e.Cancel = true;
                        return;
                    }

                    this.Invoke(new Action(() =>
                    {
                        if (backgroundWorker1.CancellationPending)
                        {
                            e.Cancel = true;
                            return;
                        }

                        int clientX = (int)((f4.Size.Width - f4.ClientSize.Width) / 2);

                        if (backgroundWorker1.CancellationPending)
                        {
                            e.Cancel = true;
                            return;
                        }

                        int clientY = f4.Size.Height - clientX - f4.ClientSize.Height;

                        if (backgroundWorker1.CancellationPending)
                        {
                            e.Cancel = true;
                            return;
                        }

                        bmpScreenshot = new Bitmap(f4.ClientSize.Width, f4.ClientSize.Height, PixelFormat.Format32bppArgb);

                        if (backgroundWorker1.CancellationPending)
                        {
                            e.Cancel = true;
                            return;
                        }

                        gfxScreenshot = Graphics.FromImage(bmpScreenshot);

                        if (backgroundWorker1.CancellationPending)
                        {
                            e.Cancel = true;
                            return;
                        }

                        gfxScreenshot.CopyFromScreen(f4.Location.X + clientX, f4.Location.Y + clientY, 0, 0, f4.ClientSize, CopyPixelOperation.SourceCopy);

                        if (backgroundWorker1.CancellationPending)
                        {
                            e.Cancel = true;
                            return;
                        }

                        TesseractEngine engine = new TesseractEngine("./tessdata", "eng", EngineMode.Default);

                        if (backgroundWorker1.CancellationPending)
                        {
                            e.Cancel = true;
                            return;
                        }

                        engine.SetVariable("tessedit_char_whitelist", "0123456789.-:");

                        if (backgroundWorker1.CancellationPending)
                        {
                            e.Cancel = true;
                            return;
                        }

                        Page page = engine.Process(bmpScreenshot, PageSegMode.Auto);

                        if (backgroundWorker1.CancellationPending)
                        {
                            e.Cancel = true;
                            return;
                        }

                        string result = page.GetText().Replace(" ", string.Empty);

                        if (backgroundWorker1.CancellationPending)
                        {
                            e.Cancel = true;
                            return;
                        }

                        var index = f3.dataGridView1.Rows.Add();

                        if (backgroundWorker1.CancellationPending)
                        {
                            e.Cancel = true;
                            return;
                        }

                        f3.dataGridView1.Rows[index].Cells["Column1"].Value = i;

                        if (backgroundWorker1.CancellationPending)
                        {
                            e.Cancel = true;
                            return;
                        }

                        f3.dataGridView1.Rows[index].Cells["Column2"].Value = result;

                        if (backgroundWorker1.CancellationPending)
                        {
                            e.Cancel = true;
                            return;
                        }

                        f3.dataGridView1.Rows[index].Cells["Column3"].Value = bmpScreenshot;

                        if (backgroundWorker1.CancellationPending)
                        {
                            e.Cancel = true;
                            return;
                        }

                    }));

                    this.Invoke(new Action(() =>
                    {
                        f3.progressBar1.Value = Convert.ToInt32(Convert.ToDouble(Convert.ToInt32(f3.dataGridView1.Rows.Count)) / Convert.ToDouble((Convert.ToInt32(textBox5.Text))) * 1000);
                    }
                    ));

                    if (backgroundWorker1.CancellationPending)
                    {
                        e.Cancel = true;
                        return;
                    }

                    Cursor.Position = new Point(Convert.ToInt16(textBox2.Text), Convert.ToInt16(textBox3.Text));

                    if (backgroundWorker1.CancellationPending)
                    {
                        e.Cancel = true;
                        return;
                    }

                    if (clickOnce == true)
                    {
                        mouse_event(MOUSEEVENTF_LEFTDOWN | MOUSEEVENTF_LEFTUP, Convert.ToUInt16(textBox2.Text), Convert.ToUInt16(textBox3.Text), 0, 0);

                        if (backgroundWorker1.CancellationPending)
                        {
                            e.Cancel = true;
                            return;
                        }

                        System.Threading.Thread.Sleep(TimeSpan.FromSeconds(2));
                    }
                    else
                    {
                        mouse_event(MOUSEEVENTF_LEFTDOWN | MOUSEEVENTF_LEFTUP, Convert.ToUInt16(textBox2.Text), Convert.ToUInt16(textBox3.Text), 0, 0);

                        if (backgroundWorker1.CancellationPending)
                        {
                            e.Cancel = true;
                            return;
                        }

                        System.Threading.Thread.Sleep(TimeSpan.FromMilliseconds(100));

                        if (backgroundWorker1.CancellationPending)
                        {
                            e.Cancel = true;
                            return;
                        }

                        mouse_event(MOUSEEVENTF_LEFTDOWN | MOUSEEVENTF_LEFTUP, Convert.ToUInt16(textBox2.Text), Convert.ToUInt16(textBox3.Text), 0, 0);

                        if (backgroundWorker1.CancellationPending)
                        {
                            e.Cancel = true;
                            return;
                        }

                        System.Threading.Thread.Sleep(TimeSpan.FromSeconds(2));
                    }

                    if (backgroundWorker1.CancellationPending)
                    {
                        e.Cancel = true;
                        return;
                    }

                    serialPort1.Write("C" + textBox1.Text);

                    if (backgroundWorker1.CancellationPending)
                    {
                        e.Cancel = true;
                        return;
                    }

                    logic = true;

                    if (backgroundWorker1.CancellationPending)
                    {
                        e.Cancel = true;
                        return;
                    }

                    for (UInt64 x = 0; x < 2000000000; x++)
                    {
                        System.Threading.Thread.Sleep(1);

                        if (backgroundWorker1.CancellationPending)
                        {
                            e.Cancel = true;
                            return;
                        }

                        if (logic == false) break;

                        if (backgroundWorker1.CancellationPending)
                        {
                            e.Cancel = true;
                            return;
                        }
                    }

                    if (backgroundWorker1.CancellationPending)
                    {
                        e.Cancel = true;
                        return;
                    }

                    logic = true;

                    if (backgroundWorker1.CancellationPending)
                    {
                        e.Cancel = true;
                        return;
                    }

                    System.Threading.Thread.Sleep(TimeSpan.FromSeconds(Convert.ToInt16(textBox4.Text)));

                    if (backgroundWorker1.CancellationPending)
                    {
                        e.Cancel = true;
                        return;
                    }
                }

                Cursor.Position = new Point(Convert.ToInt16(textBox2.Text), Convert.ToInt16(textBox3.Text));

                    if (backgroundWorker1.CancellationPending)
                    {
                        e.Cancel = true;
                        return;
                    }

                    if (clickOnce == true)
                    {
                        mouse_event(MOUSEEVENTF_LEFTDOWN | MOUSEEVENTF_LEFTUP, Convert.ToUInt16(textBox2.Text), Convert.ToUInt16(textBox3.Text), 0, 0);

                        if (backgroundWorker1.CancellationPending)
                        {
                            e.Cancel = true;
                            return;
                        }

                        System.Threading.Thread.Sleep(TimeSpan.FromSeconds(2));
                    }
                    else
                    {
                        mouse_event(MOUSEEVENTF_LEFTDOWN | MOUSEEVENTF_LEFTUP, Convert.ToUInt16(textBox2.Text), Convert.ToUInt16(textBox3.Text), 0, 0);

                        if (backgroundWorker1.CancellationPending)
                        {
                            e.Cancel = true;
                            return;
                        }

                        System.Threading.Thread.Sleep(TimeSpan.FromMilliseconds(100));

                        if (backgroundWorker1.CancellationPending)
                        {
                            e.Cancel = true;
                            return;
                        }

                        mouse_event(MOUSEEVENTF_LEFTDOWN | MOUSEEVENTF_LEFTUP, Convert.ToUInt16(textBox2.Text), Convert.ToUInt16(textBox3.Text), 0, 0);

                        if (backgroundWorker1.CancellationPending)
                        {
                            e.Cancel = true;
                            return;
                        }

                        System.Threading.Thread.Sleep(TimeSpan.FromSeconds(2));

                        if (backgroundWorker1.CancellationPending)
                        {
                            e.Cancel = true;
                            return;
                        }
                    }

            }
            catch (Exception ex)
            {
                MessageBox.Show(ex.Message, "Error", MessageBoxButtons.OK, MessageBoxIcon.Error);
            }
        }

